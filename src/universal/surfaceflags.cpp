const char* Com_SurfaceTypeToName(int iTypeIndex)
{
	if ( iTypeIndex < SURF_TYPE_BARK || iTypeIndex > SURF_TYPE_ASPHALT )
		return "default";
	else
		return infoParms[iTypeIndex].name;
}